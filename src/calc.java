public class calc{

    //Metodo para sumar 2 numeros
    public int suma(int n1, int n2){
        return n1 + n2;
    }

    //Metodo para restar 2 numeros
    public int resta(int n1, int n2){
        return n1 - n2;
    }

    //Metodo para multiplicar 2 numeros
    public int multiplicacion(int n1, int n2){
        return n1 * n2;
    }
}
